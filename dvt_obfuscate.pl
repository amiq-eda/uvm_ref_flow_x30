my $output_dir;

sub obfuscate_fh {
	my $in_fh  = $_[0];
	my $out_fh = $_[1];
	foreach (<$in_fh>) {
		print $out_fh obfuscate_line($_);
	}
}

sub obfuscate_line {
	my $line = $_[0];
	$line =~ s/\b(?<!'|\$)([a-zA-Z_][a-zA-Z0-9_]*)\b/compute_replacement($1)/eg;
	return $line;
}

if ( @ARGV == 0 ) {
	die $Usage;
}

getopts("hsfpo:k:i:");

if ($opt_h) {
	die $Usage;
}

foreach (split(",", $opt_k)) {
	$keywords{$_} = 1;
}

if ($opt_s) {
	print "// Paste some text and hit Ctrl+D to finish\n";
	obfuscate_fh( \*STDIN, \*STDOUT );
	die;
}

foreach (@ARGV) {
	if ( -e and -f ) {

		if ($opt_p) {
			$file_map{ abs_path($_) } = obfuscate_line( abs_path($_) );
		} else {
			$file_map{ abs_path($_) } = abs_path($_);
		}

	}
	else {
		print "$_ is not a file or known argument!\n";
		die $Usage;
	}
}

$output_dir = $opt_o;

if ( not defined $output_dir ) {
	$output_dir = "./dvt_obfuscator_output";
	print "Output dir not specified, defaulting to $output_dir";
}

if ( -e $output_dir ) {
	if ($opt_f) {
		print "Output dir $output_dir already exists, overwriting...";
		sleep(3);
		rmtree($output_dir);
	}
	else {
		die
"Output dir $output_dir already exists, please specify -f to overwrite.";
	}
}

mkpath($output_dir);

foreach my $key ( keys %file_map ) {

	$out_filename = $output_dir . $file_map{$key};
	mkpath( dirname($out_filename) );

	open IN, "<", $key or die "Unable to open $key for reading";
	open OUT, ">", $out_filename
	  or die "Unable to open $out_filename for writing";

	obfuscate_fh( \*IN, \*OUT );
}

