USAGE

$0 -h
	Display this help.

$0 [-o <output_dir>] [-f] [-k keyword] [-p] file1 file2 ... fileN
	Obfuscate file1 file2 ... fileN and create the obfuscated files in output_dir.
	If output dir is not specified, it defaults to ./dvt_obfuscator_output.
	If output dir exists you can specify -f to overwrite it.
